class Ex40_03 {
    public static void main(String [] args) {
        System.out.println(devide(1,1));
        System.out.println(devide(1,2));
        System.out.println(devide(2,1));
    }

    public static Float devide(int a, int b) {
        return (float) a / b;
    }
}
