public class Ex43_03 {
    public static void main(String[] args) {
        System.out.println("#### a:");
        int i = 2;
        while (i <= 12) {
            System.out.println(i);
            i++;
        }
        System.out.println("#### b:");
        i = 12;
        while (i >= 2) {
            System.out.println(i);
            i--;
        }
    }
}
