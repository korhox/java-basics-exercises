public class PrintTens {
    public static void main(String [] args) {
        // Tehtävä 12
        int i = 1;
        while (i <= 10) {
            System.out.println("Numero " + i);
            i++;
        }

        // Tehtävä 13
        i = 10;
        while (i >= 1) {
            System.out.println("Numero " + i);
            i--;
        }
    }
}